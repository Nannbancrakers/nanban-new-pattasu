import os

files_to_fix = ['index.html', 'products.html', 'about.html', 'safety.html', 'contact.html']

garbled_text = '???? ?????????? ??????? ?????????? ?????? ???????? ???????????...<br>? ????? ?????? ??????????...'
correct_text = 'ஸ்ரீ தனலெட்சுமி பட்டாசு தொழிற்சாலை தங்களை அன்புடன் வரவேற்கிறது...<br>✅ தரமான நாட்டு பட்டாசுகள்...'

for file in files_to_fix:
    if os.path.exists(file):
        with open(file, 'r', encoding='utf-8') as f:
            content = f.read()
        
        content = content.replace(garbled_text, correct_text)
        
        with open(file, 'w', encoding='utf-8') as f:
            f.write(content)
        print(f"Fixed {file}")
